package com.h10_fams.amaderdoctor.Models;

public class Lipid {
    private int LDL, HDL, VLDL;

    public int getLDL() {
        return LDL;
    }

    public void setLDL(int LDL) {
        this.LDL = LDL;
    }

    public int getHDL() {
        return HDL;
    }

    public void setHDL(int HDL) {
        this.HDL = HDL;
    }

    public int getVLDL() {
        return VLDL;
    }

    public void setVLDL(int VLDL) {
        this.VLDL = VLDL;
    }
}
