package com.h10_fams.amaderdoctor.Models;

public class BloodCount {
    private int rbc, wbc, platelets;

    public int getRbc() {
        return rbc;
    }

    public void setRbc(int rbc) {
        this.rbc = rbc;
    }

    public int getWbc() {
        return wbc;
    }

    public void setWbc(int wbc) {
        this.wbc = wbc;
    }

    public int getPlatelets() {
        return platelets;
    }

    public void setPlatelets(int platelets) {
        this.platelets = platelets;
    }
}
