package com.h10_fams.amaderdoctor.Models;

public class Hormone {
    private int adrenaline, testosterone, destrogen, oxytocin, cortisol;

    public int getAdrenaline() {
        return adrenaline;
    }

    public void setAdrenaline(int adrenaline) {
        this.adrenaline = adrenaline;
    }

    public int getTestosterone() {
        return testosterone;
    }

    public void setTestosterone(int testosterone) {
        this.testosterone = testosterone;
    }

    public int getDestrogen() {
        return destrogen;
    }

    public void setDestrogen(int destrogen) {
        this.destrogen = destrogen;
    }

    public int getOxytocin() {
        return oxytocin;
    }

    public void setOxytocin(int oxytocin) {
        this.oxytocin = oxytocin;
    }

    public int getCortisol() {
        return cortisol;
    }

    public void setCortisol(int cortisol) {
        this.cortisol = cortisol;
    }
}
